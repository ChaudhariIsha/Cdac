class TypeCast{
 
 public static void main(String args[]){
 
  int a = 10;
  double  d = a;
    
   
   double f = 11.14d;
   int b = (int)f;
   
   System.out.println("int a value is:"+a);
   System.out.println(" After implicit typecasting Double d value is:"+d);
   System.out.println(" float  f value is:"+f);
   System.out.println(" After explicit typecasting int b value is:"+b);
   
 
 
 
 
 
 
 
 
 
 
 
 
 }}