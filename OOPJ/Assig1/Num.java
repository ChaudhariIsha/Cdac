class Num{
 public static void main(String args[]){
  
   int binary = 0b0101;
   int oct = 067;
   int hex = 0x3f;
   float f = 31.23f;
   
   System.out.println("binary (0b0101): "+binary);
   System.out.println("Octal (067): "+oct);
   System.out.println("Hexadecimal(0x3f): "+hex);
   System.out.println("float (31.23f): "+f);
  }}